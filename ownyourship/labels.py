"""Plain-language component labels for the architecture diagram.

Labels are generated by Claude and cached on disk keyed by a content fingerprint,
so a component is only re-described when its code actually changes.
"""
import json
from pathlib import Path
from typing import Dict

import anthropic

from .quiz import HAIKU_MODEL


def _looks_like_a_question(text: str) -> bool:
    """A description shouldn't be a question — that means the model asked for input
    (e.g. "could you share the source?") instead of describing the file."""
    return not text or text.rstrip().endswith("?")


def _function_listing(component: Dict) -> str:
    lines = []
    for f in component["functions"][:30]:
        sig = f.get("signature") or f["name"]
        doc = (f.get("docstring") or "").strip().splitlines()
        first = f"  # {doc[0]}" if doc else ""
        lines.append(f"- {sig}{first}")
    return "\n".join(lines) or "(no functions)"


async def generate_component_label(component: Dict, client) -> str:
    """One short sentence describing what a component (file) does."""
    prompt = (
        f"A source file named `{component['name']}` defines these functions "
        f"(signatures, with a docstring line where present):\n"
        f"{_function_listing(component)}\n\n"
        f"From the names and signatures alone, infer what this file is responsible for. "
        f"Reply with ONE short declarative sentence and nothing else. Do NOT ask for the "
        f"source code or say you lack information; give your best inference."
    )
    try:
        resp = await client.messages.create(
            model=HAIKU_MODEL,
            max_tokens=80,
            messages=[{"role": "user", "content": prompt}],
        )
    except anthropic.APIError:
        return ""
    text = resp.content[0].text.strip()
    return "" if _looks_like_a_question(text) else text


def _parse_json_object(text: str) -> Dict:
    """Parse a JSON object the model may have wrapped in a ``` fence."""
    t = text.strip()
    if t.startswith("```"):
        t = t.split("\n", 1)[1] if "\n" in t else ""
        t = t.rsplit("```", 1)[0]
    try:
        parsed = json.loads(t)
    except json.JSONDecodeError:
        return {}
    return parsed if isinstance(parsed, dict) else {}


# Functions per generation call; keeps each JSON response safely inside its
# token budget so it always parses.
_FN_LABEL_CHUNK = 16


async def generate_function_labels(file_name: str, functions: list, client) -> Dict[str, str]:
    """One-line descriptions for a file's functions, one batched call per file.

    Returns ``{function_id: label}``; ids the model missed or answered with a
    question are absent.
    """
    listing = []
    for f in functions:
        sig = f.get("signature") or f["name"]
        doc = (f.get("docstring") or "").strip().splitlines()
        first = f"  # {doc[0]}" if doc else ""
        listing.append(f"- {f['id']}: {sig}{first}")
    prompt = (
        f"These functions are defined in a source file named `{file_name}` "
        f"(one `id: signature` per line, with a docstring line where present):\n"
        + "\n".join(listing)
        + "\n\nFrom the names and signatures alone, infer what each function does. "
        "Reply with ONLY a JSON object mapping each id to ONE short declarative "
        "sentence (no markdown, no commentary). Do NOT ask for the source code or "
        "say you lack information; give your best inference."
    )
    try:
        resp = await client.messages.create(
            model=HAIKU_MODEL,
            max_tokens=90 * len(functions) + 150,
            messages=[{"role": "user", "content": prompt}],
        )
    except anthropic.APIError:
        return {}
    parsed = _parse_json_object(resp.content[0].text)
    wanted = {f["id"] for f in functions}
    # The model doesn't always echo the full ids — with long ids it keys the
    # JSON by bare function name instead. Accept a name that maps to exactly
    # one requested function; ambiguous names are dropped, never guessed.
    by_name: Dict[str, list] = {}
    for f in functions:
        by_name.setdefault(f["name"], []).append(f["id"])
    out: Dict[str, str] = {}
    for key, label in parsed.items():
        if not isinstance(label, str):
            continue
        label = label.strip()
        if _looks_like_a_question(label):
            continue
        if key in wanted:
            out[key] = label
        elif len(by_name.get(key, [])) == 1:
            out[by_name[key][0]] = label
    return out


async def attach_function_labels(diagram: Dict, function_ids: list, cache_path: Path, client) -> Dict[str, str]:
    """Labels for the requested function ids, cached by each function's content_hash.

    Cache entries live in the same file as component labels under ``fn:``-prefixed
    keys, so a description regenerates only when the function's body changes.
    """
    index = {}
    for comp in diagram["components"]:
        for f in comp["functions"]:
            index[f["id"]] = (comp, f)

    cache = _load_cache(cache_path)
    out: Dict[str, str] = {}
    misses: Dict[str, list] = {}
    for fid in dict.fromkeys(function_ids):  # dedupe, keep order
        if fid not in index:
            continue
        comp, f = index[fid]
        cached = cache.get("fn:" + fid)
        if (
            cached
            and cached.get("content_hash") == f.get("content_hash")
            and not _looks_like_a_question(cached.get("label", ""))
        ):
            out[fid] = cached["label"]
            continue
        misses.setdefault(comp["id"], []).append(f)

    for fns in misses.values():
        comp = index[fns[0]["id"]][0]
        # Bounded chunks: one call for a big file (app.js: 64 fns) truncates
        # the JSON response at max_tokens and loses every label.
        generated = {}
        for i in range(0, len(fns), _FN_LABEL_CHUNK):
            generated.update(await generate_function_labels(comp["name"], fns[i:i + _FN_LABEL_CHUNK], client))
        for f in fns:
            label = generated.get(f["id"], "")
            if label:
                out[f["id"]] = label
                cache["fn:" + f["id"]] = {"content_hash": f.get("content_hash"), "label": label}
            else:
                # Never cache a failure — the next request should retry.
                cache.pop("fn:" + f["id"], None)

    if misses:
        _save_cache(cache_path, cache)
    return out


def _load_cache(cache_path: Path) -> Dict:
    try:
        return json.loads(cache_path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return {}


def _save_cache(cache_path: Path, cache: Dict) -> None:
    try:
        cache_path.parent.mkdir(parents=True, exist_ok=True)
        cache_path.write_text(json.dumps(cache, indent=2), encoding="utf-8")
    except OSError:
        pass


async def attach_component_labels(diagram: Dict, cache_path: Path, client) -> Dict:
    """Fill each component's ``label``, generating only for changed components."""
    cache = _load_cache(cache_path)
    for comp in diagram["components"]:
        fp = comp.get("fingerprint")
        cached = cache.get(comp["id"])
        # A fingerprint match alone isn't enough: entries written before the
        # prompt hardening (#18) can hold a refusal, and the file not changing
        # would otherwise serve it forever. Re-validate the label itself.
        if cached and cached.get("fingerprint") == fp and not _looks_like_a_question(cached.get("label", "")):
            comp["label"] = cached["label"]
            continue
        label = await generate_component_label(comp, client)
        comp["label"] = label
        if label:
            cache[comp["id"]] = {"fingerprint": fp, "label": label}
        else:
            # Never cache a failure with the current fingerprint — that would
            # make it permanent. Drop any stale entry so the next run retries.
            cache.pop(comp["id"], None)
    _save_cache(cache_path, cache)
    return diagram
